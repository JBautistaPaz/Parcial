class Usuario:
    def __init__(self, id_usuario, nombre, apellido, edad, email, contraseña, fecha_nacimiento, estado):
        self.id_usuario = id_usuario
        self.nombre = nombre
        self.apellido = apellido
        self.edad = edad
        self.email = email
        self.contraseña = contraseña
        self.fecha_nacimiento = fecha_nacimiento
        self.estado = estado